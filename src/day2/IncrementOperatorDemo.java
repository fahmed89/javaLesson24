package day2;

public class IncrementOperatorDemo {
	
	public static void main(String[] args) {
		
		//Increment operator ++
		
		int a = 10;
//		a = a + 1;
//      or
		a++;
		
		System.out.println(a); //11
		
		// There are two type of increment
		// post increment
		int x = 10;
		int result = x++;
		System.out.println(result);//10
		System.out.println(x);//11
		
		//preincrement
		int b = 20;
		int res = ++b;
		System.out.println(res);//21
		
		
		
	
		
		
		
	}
	
	
	
	

}
